package com.gpdi.hqplus.common.constants;

/**
 * 流程枚举
 *
 * @author: lianghb
 * @create: 2019-06-27 14:04
 **/
public enum ProcessDefinitionEnum {

    COMPLAINT("complaint", "反馈建议"),
    PROPERTY_MAINTENANCE("property_maintenance", "物业报修"),
    PRODUCT_PASS_REVIEW("product_pass_review","物品放行");

    private String key;
    private String name;

    ProcessDefinitionEnum(String key, String name) {
        this.key = key;
        this.name = name;
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
